package module1;

public class Switchcase {

	public static void main(String[] args) {
		int a=2;
		switch(a)
		{
		  case 1: System.out.println("mahesh");
		  break;
		  case 2: System.out.println("madhu");
		  break;
		  case 3:System.out.println("madhan");
		  break;
		}
	}
}


